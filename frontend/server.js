const express = require('express');
const path = require('path');
const app = express();
const PORT = process.env.PORT || 80;

// Servir archivos estÃ¡ticos de Angular
app.use(express.static(path.join(__dirname, 'dist/skysense-dashboard')));

// Manejar todas las rutas para Angular Router
app.get('*', (req, res) => {
  res.sendFile(path.join(__dirname, 'dist/skysense-dashboard/index.html'));
});

app.listen(PORT, '0.0.0.0', () => {
  console.log(`Frontend server running on port ${PORT}`);
});
